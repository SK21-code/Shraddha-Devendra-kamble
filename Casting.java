import java.io.*;

class Geeks {
    public static void main(String[] args)
    {
        double i = 120;

        // Narrowing Type Casting
        long l = i;
        double d = i;

        System.out.println("Interger "
                           + i);
        System.out.println("Long  "
                           + l);
        System.out.println("Double  "
                           + d);
    }
}